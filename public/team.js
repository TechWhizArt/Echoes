// Function to generate member card HTML

function generateMemberCard(member) {
    return `
        <div class="photosJuniors">
            <div class="roundPhotoContainerJuniors">
                <div class="roundPhotoJuniors">
                    <img src="${member.image}" alt="${member.name}">
                </div>
                <div class="roundPhotoContainerTextJuniors membersTextJuniors">
                    <h3 class="boldTextJuniors">${member.name}</h3>
                    <div class="social-linksJuniors">

                        <a href="${member.linkedin}" target="_blank">LinkedIn</a>

                        ${member.github && member.github.trim() !== "" 
                            ? `<a href="${member.github}" target="_blank">GitHub</a>` 
                            : ""}

                    </div>
                </div>
            </div>
        </div>
    `;
}



// Function to generate category HTML
function generateCategory(categoryId, categoryData) {
    const membersHTML = categoryData.members.map(member => generateMemberCard(member)).join('');

    return `
            <div class="categoryJuniors" data-category="${categoryId}">
                <h3 class="category-titleJuniors">${categoryData.title}</h3>
                <div class="photosParentContainerJuniors membersPhotosJuniors">
                    ${membersHTML}
                </div>
            </div>
        `;
}

// Function to render all categories
function renderCategories() {
    const container = document.getElementById('categoriesContainer');
    let categoriesHTML = '';

    for (const [categoryId, categoryData] of Object.entries(memberData)) {
        categoriesHTML += generateCategory(categoryId, categoryData);
    }

    container.innerHTML = categoriesHTML;
}

// Function to filter categories
function filterCategories(category) {
    const allCategories = document.querySelectorAll('.categoryJuniors');

    if (category === 'all') {
        allCategories.forEach(cat => {
            cat.classList.remove('hidden');
            setTimeout(() => {
                cat.style.display = 'block';
            }, 50);
        });
    } else {
        allCategories.forEach(cat => {
            if (cat.dataset.category === category) {
                cat.classList.remove('hidden');
                setTimeout(() => {
                    cat.style.display = 'block';
                }, 50);
            } else {
                cat.classList.add('hidden');
                setTimeout(() => {
                    cat.style.display = 'none';
                }, 500);
            }
        });
    }
}

// Initialize the page
document.addEventListener('DOMContentLoaded', function () {
    // Render all categories
    renderCategories();

    // Add event listeners to filter buttons
    const filterButtons = document.querySelectorAll('.filter-btnJuniors');

    filterButtons.forEach(button => {
        button.addEventListener('click', function () {
            // Remove active class from all buttons
            filterButtons.forEach(btn => btn.classList.remove('active'));
            // Add active class to clicked button
            this.classList.add('active');

            // Filter categories
            const category = this.dataset.category;
            filterCategories(category);
        });
    });
});

